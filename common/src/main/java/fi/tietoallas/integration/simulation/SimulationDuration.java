/**
 * The duration of a simulation run.
 */
public enum SimulationDuration {

    /** The simulation loops infinitely */
    INFINITE,

    /** The simulation executes a fixed amount of iterations and exits. */
    FIXED,

    /** The simulation consumes all data and exits. */
    CONSUME_ALL
}
